#include <unistd.h>

void	ft_putnbr(int nb);

void	ft_putnbr(int nb)
{
	char	dec;
	char	unit;

	dec = (nb / 10) + '0';
	unit = (nb % 10) + '0';
	if (nb >= 0 && nb <= 9)
	{
		write(1, &unit, 1);
	}
	else
	{
		write(1, &dec, 1);
		write(1, &unit, 1);
	}
}

int	main(void)
{
	int	i;

	i = 1;
	while (i <= 100)
	{
		if (i % 3 == 0 && i % 5 == 0)
			write(1, "fizzbuzz", 8);
		else if (i % 3 == 0)
			write(1, "fizz", 4);
		else if (i % 5 == 0)
			write(1, "buzz", 4);
		else
			ft_putnbr(i);
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
